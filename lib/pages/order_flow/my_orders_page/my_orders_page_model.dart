  PagingController<ApiPagingParams, dynamic> _createListViewController(
    Function(ApiPagingParams) query,
  ) {
    final controller = PagingController<ApiPagingParams, dynamic>(
      firstPageKey: ApiPagingParams(
        nextPageNumber: 0,
        numItems: 0,
        lastResponse: null,
      ),
    );
    return controller..addPageRequestListener(listViewGetOrdersPage);
  }

  void listViewGetOrdersPage(ApiPagingParams nextPageMarker) =>
      listViewApiCall!(nextPageMarker).then((listViewGetOrdersResponse) {
        final pageItems = (PlantShopGroup.getOrdersCall
                    .orderList(
                      listViewGetOrdersResponse.jsonBody,
                    )!
                    .where((e) =>
                        'checkout-draft' !=
                        getJsonField(
